export function Discussion(state = InitialState, action) {
    switch (action.type) {
        case Addnote:
            return {
                ...state,
                PostDetails: [action.newNote, ...state.PostDetails],
            };
            break;
        case Delnote:
            const filtered = state.PostDetails.filter((noteItem, index) => {
                return index !== action.id;
            });

            return {
                ...state,
                PostDetails: filtered,
            };

            break;
        case Comments:
            const { Name, Email, content: comm, value } = action.obj;
            const id = action.Id;
            const objnew = {
                name: Name,
                email: Email,
                content: comm,
                tm: value,
            };

            return {
                ...state,
                PostDetails: state.PostDetails.map((content, i) =>
                    i === id
                        ? { ...content, comment: [objnew, ...content.comment] }
                        : content
                ),
            };

            break;
        case LikeDisLike:
            var idval = action.Id;

            switch (action.operation) {
                case "L+":

                    let newOBJ = state.PostDetails.map((content, i) =>
                        i === idval
                            ? {
                                ...content,
                                L: action.Lvalue,
                                DL: action.DLvalue,
                                Like: action.setLiked,
                                DisLike: true,
                            }
                            : content
                    );

                    return {
                        ...state,
                        PostDetails: newOBJ,
                    };
                    break;
                case "DL+":
                    let newOBJ2 = state.PostDetails.map((content, i) =>
                        i === idval
                            ? {
                                ...content,
                                L: action.Lvalue,
                                DL: action.DLvalue,
                                DisLike: action.setLiked,
                                Like: true,
                            }
                            : content
                    );

                    return {
                        ...state,
                        PostDetails: newOBJ2,
                    };
                    break;

                case "DL-&L+":
                    let newObj1 = state.PostDetails.map((content, i) =>
                        i === idval
                            ? {
                                ...content,
                                L: action.Lvalue,
                                DL: action.DLvalue,
                                Like: action.setLiked,
                                DisLike: true,
                            }
                            : content
                    );

                    return {
                        ...state,
                        PostDetails: newObj1,
                    };
                    break;
                case "DL+&L-":
                    let newObj2 = state.PostDetails.map((content, i) =>
                        i === idval
                            ? {
                                ...content,
                                L: action.Lvalue,
                                DL: action.DLvalue,
                                DisLike: action.setLiked,
                                Like: true,
                            }
                            : content
                    );

                    return {
                        ...state,
                        PostDetails: newObj2,
                    };
                    break;

                default:
                    return state;
                    break;
            }

        default:
            return state;
            break;
    }
}

export default Discussion;
